var fs = require("fs");
var path = require("path");

function writeModule(fname, words) {
  var lines = ["module.exports = ["];
  for (var i = 0; i < words.length; i++) {
    var w = words[i];
    lines.push(
      '  ["' +
        w[0] +
        '", "' +
        w[1] +
        '", "' +
        w[2] +
        '"]' +
        (i < words.length - 1 ? "," : ""),
    );
  }
  lines.push("];\n");
  var content = lines.join("\n");
  var fp = path.join("E:/CET/tools", fname);
  fs.writeFileSync(fp, content, "utf-8");
  console.log(
    fname +
      ": " +
      words.length +
      " words, " +
      content.split("\n").length +
      " lines",
  );
}

